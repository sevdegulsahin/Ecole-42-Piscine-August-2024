void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	b;

	i = 0;
	while (i < size - 1)
	{
		b = tab[i];
		tab[i] = tab[size - 1];
		tab[size - 1] = b;
		i++;
		size--;
	}
}
